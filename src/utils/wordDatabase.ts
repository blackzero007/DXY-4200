export function getRandomStartWord(): string {
  return START_WORDS[Math.floor(Math.random() * START_WORDS.length)];
}

export function getLastChar(word: string): string {
  if (!word || word.length === 0) return '';
  return word.charAt(word.length - 1);
}

export function getFirstChar(word: string): string {
  if (!word || word.length === 0) return '';
  return word.charAt(0);
}

export function charMatch(charA: string, charB: string, allowHomophone: boolean = true): boolean {
  if (charA === charB) return true;
  if (!allowHomophone) return false;
  const variants = HOMOPHONE_MAP[charA];
  if (variants && variants.includes(charB)) return true;
  const variantsB = HOMOPHONE_MAP[charB];
  if (variantsB && variantsB.includes(charA)) return true;
  return false;
}

export function containsLastChar(word: string, lastChar: string, allowHomophone: boolean = true): boolean {
  if (!word || !lastChar) return false;
  for (const ch of word) {
    if (charMatch(ch, lastChar, allowHomophone)) return true;
  }
  return false;
}
